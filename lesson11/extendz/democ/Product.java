package lesson11.extendz.democ;

public class Product {

    private String name;
    private int qnty;

    public Product(String name, int qnty) {
        this.name = name;
        this.qnty = qnty;
    }

    public String getName() {
        return name;
    }

    public int getQnty() {
        return qnty;
    }

}
