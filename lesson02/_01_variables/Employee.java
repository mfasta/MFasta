package lesson02._01_variables;

public class Employee {

    // Ключевое слово static в Java используется,
    // в основном, для управления памятью.
    // Если вы объявляете переменную как статическую,
    // она называется статической переменной.
    // Статическая переменная (переменная класса).
    // Статическая переменная получает память только один раз
    // в области класса во время загрузки класса.
    public static float salary;
    public static String name = "Денис";
}
